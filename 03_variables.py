# In Python, variables are used to store data values.
# A variable is created by assigning a value to it using the equals sign (=).
# The variable name can be any combination of letters, numbers, and underscores, but it cannot start with a number.
# Variable names are case-sensitive, meaning that "myVariable" and "myvariable" would be considered different variables.
# For example, you can assign an integer value to a variable and later assign a string value to the same variable.



