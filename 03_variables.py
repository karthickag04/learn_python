# 5. Avoid using special characters: Variable names should only contain letters, numbers, and underscores. Avoid using spaces or special characters (e.g., @, #, $, %).

# 6. Use camelCase or snake_case: Choose a naming style (camelCase or snake_case) and stick to it. For example, "myVariable" (camelCase) or "my_variable" (snake_case).

